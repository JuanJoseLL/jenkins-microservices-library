    def installGitHubCLI() {
        steps.echo "ğŸ“¦ Verificando GitHub CLI..."
        
        def ghInstalled = steps.sh(
            script: "command -v gh",
            returnStatus: true
        ) == 0
        
        if (!ghInstalled) {
            steps.echo "ğŸ“¦ Instalando GitHub CLI..."
            steps.sh """
                curl -fsSL https://cli.github.com/packages/githubcli-archive-keyring.gpg | sudo dd of=/usr/share/keyrings/githubcli-archive-keyring.gpg
                echo "deb [arch=\$(dpkg --print-architecture) signed-by=/usr/share/keyrings/githubcli-archive-keyring.gpg] https://cli.github.com/packages stable main" | sudo tee /etc/apt/sources.list.d/github-cli.list > /dev/null
                sudo apt update && sudo apt install gh -y
            """
        } else {
            steps.echo "âœ… GitHub CLI ya estÃ¡ instalado"
        }
    }
    
    def createRelease(String tag, String title, String notes, String token) {
        steps.echo "ğŸ·ï¸  Creando release ${tag} en GitHub..."
        
        // Instalar CLI si es necesario
        installGitHubCLI()
        
        // Autenticar con GitHub
        steps.sh """
            echo "${token}" | gh auth login --with-token
        """
        
        // Crear el release
        steps.sh """
            gh release create "${tag}" \
                --title "${title}" \
                --notes "${notes}" \
                --latest
        """
        
        steps.echo "âœ… Release ${tag} creado exitosamente"
    }
    
